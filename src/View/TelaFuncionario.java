    private void grava() {
        if (tfNomeFunc.getText().isEmpty() || tfCPFFunc.getText().isEmpty() || tfSalarioFunc.getText().isEmpty()
                || tfDnFunc.getText().isEmpty() || tfDrFunc.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Campos necessarios não preenchidos");
        } else {
            String nome = tfNomeFunc.getText();
            String cpf = tfCPFFunc.getText();
            String cel = tfCelularFunc.getText();
            String email = tfEmailFunc.getText();
            String data_nasc = tfDnFunc.getText();
            String data_registro = tfDrFunc.getText();
            Cargo cargo = null;
            cargo = (Cargo) cbCargo.getSelectedItem();
            double salario = Converte.textoToValue(tfSalarioFunc.getText());
            boolean Ativo = CheckAtivo.isSelected();
            if (funcionarioDAO.create(new Funcionario(cargo, Ativo, salario, nome, email, cpf, data_nasc, cel, data_registro))) {
                JOptionPane.showMessageDialog(rootPane, "Sucesso");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Falhou, tente novamente");
            }
        }
        limpa();
    }

    private void busca() {
        func = funcionarioDAO.find(tfBuscarFunc.getText());
        String ativo = "Inativo";
        if(func.isAtivo()){
            ativo =  "Ativo";
        }
        DefaultTableModel model = (DefaultTableModel) tbFunc.getModel();
        model.setNumRows(0);
        
        model.addRow(new Object[]{
            func.getNome(),
            func.getCpf(),
            func.getRegistro(),
            func.getCelular(),
            func.getEmail(),
            func.getCargo().getNome_cargo(),
            func.getSalario(),
            ativo
        });
        tbFunc.setModel(model);
    }
    
    private void carregaFuncionario(){
        listaFunc = (ArrayList<Funcionario>) funcionarioDAO.read();
        String ativo = "Inativo";
        DefaultTableModel model = (DefaultTableModel) tbFunc.getModel();
        model.setNumRows(0);
        
        for(Funcionario fun:listaFunc){            
            if(fun.isAtivo()){
                ativo =  "Ativo";
            }
            model.addRow(new Object[]{
                fun.getNome(),
                fun.getCpf(),
                fun.getRegistro(),
                fun.getCelular(),
                fun.getEmail(),
                fun.getCargo().getNome_cargo(),
                fun.getSalario(),
                ativo
            });
        }
        tbFunc.setModel(model);
    }

    private void deleta() {
        if(tfCPFFunc.getText().equals("___.___.___-__")){
            JOptionPane.showMessageDialog(rootPane, "Não foi possivel executar essa função");
        }else{
            dao.FuncionarioDAO.getInstance().delete(tfCPFFunc.getText());
            limpa();
        }
    }

