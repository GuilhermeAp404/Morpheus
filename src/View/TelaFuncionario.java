    private void tbFuncMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbFuncMouseClicked
        String padrao = "##.##";
        DecimalFormat df = new DecimalFormat(padrao);
        int row = tbFunc.getSelectedRow();
        String cpf = (String) tbFunc.getValueAt(row, 1);
        func = funcionarioDAO.find(cpf);
        //preenche campos
        tfNomeFunc.setText(func.getNome());
        tfCPFFunc.setText(func.getCpf());
        tfCelularFunc.setText(func.getCelular());
        tfEmailFunc.setText(func.getEmail());
        tfDnFunc.setText(func.getData_nascimento());
        tfDrFunc.setText(func.getRegistro());
        cbCargo.getModel().setSelectedItem(func.getCargo());
        tfSalarioFunc.setText(df.format(func.getSalario()));
        CheckAtivo.setSelected(func.isAtivo());
        
    }//GEN-LAST:event_tbFuncMouseClicked

